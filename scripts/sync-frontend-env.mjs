import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, '..');
const envPath = path.join(repoRoot, '.env');
const envExamplePath = path.join(repoRoot, '.env.example');
const outputPath = path.join(repoRoot, 'apps', 'frontend', 'src', 'app', 'core', 'frontend-env.ts');

function parseEnvFile(fileContent) {
  const values = {};

  for (const rawLine of fileContent.split(/\r?\n/u)) {
    const line = rawLine.trim();

    if (!line || line.startsWith('#')) {
      continue;
    }

    const separatorIndex = line.indexOf('=');

    if (separatorIndex <= 0) {
      continue;
    }

    const key = line.slice(0, separatorIndex).trim();
    let value = line.slice(separatorIndex + 1).trim();

    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }

    values[key] = value;
  }

  return values;
}

const sourcePath = fs.existsSync(envPath) ? envPath : envExamplePath;

if (!fs.existsSync(sourcePath)) {
  throw new Error('No .env or .env.example file found at the repository root.');
}

const parsedEnv = parseEnvFile(fs.readFileSync(sourcePath, 'utf8'));
const backendPort = parsedEnv.PORT || '3000';
const apiBaseUrl = parsedEnv.PUBLIC_API_BASE_URL || `http://localhost:${backendPort}/api/v1`;
const assetBaseUrl = parsedEnv.PUBLIC_ASSET_BASE_URL || `http://localhost:${backendPort}`;

const frontendEnv = {
  apiBaseUrl,
  assetBaseUrl,
  firebasePublicConfig: {
    apiKey: parsedEnv.PUBLIC_FIREBASE_API_KEY || '',
    authDomain: parsedEnv.PUBLIC_FIREBASE_AUTH_DOMAIN || '',
    projectId: parsedEnv.PUBLIC_FIREBASE_PROJECT_ID || parsedEnv.FIREBASE_PROJECT_ID || '',
    appId: parsedEnv.PUBLIC_FIREBASE_APP_ID || '',
  },
};

const output = `// This file is generated by scripts/sync-frontend-env.mjs.\n// Do not edit it manually. Update the repository root .env instead.\nexport const frontendEnv = ${JSON.stringify(frontendEnv, null, 2)} as const;\n`;

fs.writeFileSync(outputPath, output, 'utf8');
console.log(`Synced frontend env from ${path.basename(sourcePath)}.`);